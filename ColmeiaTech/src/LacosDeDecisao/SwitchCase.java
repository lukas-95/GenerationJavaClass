package LacosDeDecisao;


public class SwitchCase {

    public static void main(String[] args) {


        switch (4) {
            case 1:
                System.out.println("manhã");
                break;
            case 2:
                System.out.println("tarde");
                break;
            case 3:
                System.out.println("noite");
                break;
            default:
                System.out.println("opção inválida");

        }
    }
}
